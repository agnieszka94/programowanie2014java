package pl.edu.uj.tcs.programowanie06.creatingobjects;

/**
 * A class with the default constructor created by the compiler.
 * 
 * @author kozik
 * 
 */
public class Class01DefaultConstructor {

	public void itWorks() {
		System.out.println("It works!");
	}

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		new Class01DefaultConstructor().itWorks();
	}
}
